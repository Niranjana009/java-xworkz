package com.xworkz.core_java.OOps.Polymorphism.overloading.add_ex;

public class Add {

	int add (int a, int b) {
		return a+b;
	}
	
	float add(float a, float b) {
		return a+b;
	}
	
	double add(double a, double b, double c) {
		return a+b+c;
	}
	
	int add(int a, int b, int c ) {
		return a+b+c;
	}
	
}
