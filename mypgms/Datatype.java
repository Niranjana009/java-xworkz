
class Datatype{
	
	public static void main(String[] args){
		byte var1 = 10;
		short var2 = 20;
		int var3 = 30;
		long var4 = 40l;
		
		float var5 = 4.4f;
		double var6 = 5.5;
		
		char var7 = 'c';
		boolean var8 = true;
		
		
		System.out.println(	"\n"+"==========Integer Datatype=========="
							+"\n\t"+"Byte defined variable is: " + var1+
							"\n\t"+"Short defined variable is: " + var2+
							"\n\t"+"Integer defined variable is: " + var3+
							"\n\t"+"Long defined variable is: " + var4);
							
		
		System.out.println(	"\n"+"==========Floating point Datatype=========="
							+"\n\t"+"Float defined variable is: " + var5+
							"\n\t"+"Double defined variable is: " + var6);
							
		
		System.out.println(	"\n"+"==========Character Datatype=========="
							+"\n\t"+"Char defined variable is: " + var7);
							
		
		System.out.println(	"\n"+"==========Boolean Datatype=========="
							+"\n\t"+"Boolean defined variable is: " + var8);
	
}
}